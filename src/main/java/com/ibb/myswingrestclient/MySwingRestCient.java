    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        person.setFirstname(jTextField1.getText());
        person.setLastname(jTextField2.getText());
        person.getAddress().setCity(jTextField3.getText());
        person.getAddress().setStreet(jTextField4.getText());
        if (new RestServiceClass().sendService(person)) {
            person = new Person();
            jLabel4.setText("Daten erfolgreich gesendet!");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        } else {
            jLabel4.setText("Daten N I C H T erfolgreich gesendet!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

